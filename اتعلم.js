module.exports = {
  category: 'tools',
  command: 'اتعلم',
  description: 'يعطي معلومة هل تعلم',
  async execute(sock, msg) {
    const from = msg.key.remoteJid;

    const facts = [
      'هل تعلم أن القلب يضخ حوالي 7600 لتر من الدم يوميًا؟',
      'هل تعلم أن الأخطبوط لديه ثلاثة قلوب؟',
      'هل تعلم أن النحل يرى الأشعة فوق البنفسجية؟',
      'هل تعلم أن الفضاء ليس صامتًا كما نظن، بل فيه موجات؟',
      'هل تعلم أن أطول كلمة في اللغة العربية تحتوي على 15 حرفًا؟',
      'هل تعلم أن أول حاسوب تم إنشاؤه كان يزن أكثر من 27 طن؟',
      'هل تعلم أن الإنسان يستطيع العيش بدون طعام لأكثر من 3 أسابيع؟',
      'هل تعلم أن الضحك يحفز الجسم على إفراز الإندورفين؟',
      'هل تعلم أن القمر يبتعد عن الأرض بمقدار 3.8 سم سنويًا؟',
      'هل تعلم أن الماء الساخن يمكن أن يتجمد أسرع من البارد أحيانًا؟ (تأثير مبمبا)'
    ];

    const randomFact = facts[Math.floor(Math.random() * facts.length)];

    await sock.sendMessage(from, { text: `🧠 ${randomFact}` }, { quoted: msg });
  }
};