const ahadith = [
  `╭───⊰ 📜 *حديث نَبَوِيّ* ⊱───╮
"تَبَسُّمك في وجه أخيك صدقة."
╰────────────╯`,
  `╭───⊰ 📜 *حديث نَبَوِيّ* ⊱───╮
"الدّالّ على الخير كفاعله."
╰────────────╯`,
  `╭───⊰ 📜 *حديث نَبَوِيّ* ⊱───╮
"لا يؤمن أحدكم حتى يُحب لأخيه ما يحب لنفسه."
╰────────────╯`,
  `╭───⊰ 📜 *حديث نَبَوِيّ* ⊱───╮
"من لا يَرحم لا يُرحم."
╰────────────╯`,
  `╭───⊰ 📜 *حديث نَبَوِيّ* ⊱───╮
"يَسّروا ولا تعسّروا، وبشّروا ولا تنفّروا."
╰────────────╯`
];

module.exports = {
  command: 'أحاديثي',
  description: '📜 حديث نبوي مزخرف',
  usage: '.أحاديثي',
  category: 'ديني',

  async execute(sock, msg) {
    const text = ahadith[Math.floor(Math.random() * ahadith.length)];
    await sock.sendMessage(msg.key.remoteJid, { text }, { quoted: msg });
  }
};